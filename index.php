<script>
    document.addEventListener('DOMContentLoaded', () => {
        const counters = document.querySelectorAll('.count-bar');
        let observer;

        function resetCounters() {
            counters.forEach(counter => {
                const count = +counter.getAttribute('data-count');
                let currentCount = 0;
                const increment = Math.ceil(count / 100);
                
                // Set width based on count
                counter.style.width = `${150 + count / 10}px`;

                // Create a number element for displaying count
                const numberElement = counter.nextElementSibling;

                const updateCounter = () => {
                    if (currentCount < count) {
                        currentCount += increment;
                        numberElement.innerText = currentCount > count ? count : '+' + currentCount;
                        setTimeout(updateCounter, 20);
                    } else {
                        numberElement.innerText = '+' + count;
                    }
                };
                updateCounter();
            });
        }

        const section = document.querySelector('.chart-container');

        observer = new IntersectionObserver(entries => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    resetCounters();
                }
            });
        });

        if (section) {
            observer.observe(section);
        }
    });
</script>


